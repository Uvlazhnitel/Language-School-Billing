export function normalizeLocale(locale?: string | null): UiLocale {
  if (locale === "lv-LV") return "lv-LV";
  if (locale === "ru-RU") return "ru-RU";
  return "lv-LV";
}

export function createTranslator(locale: UiLocale): TranslateFn {
  return (key, params) => {
    const template =
      translations[locale][key] ?? translations["lv-LV"][key] ?? translations["en-US"][key] ?? key;
    if (!params) return template;
    return Object.entries(params).reduce(
      (result, [name, value]) => result.replaceAll(`{${name}}`, String(value)),
      template
    );
  };
}

export function getMonthNames(locale: UiLocale): string[] {
  return monthNames[locale];
}
